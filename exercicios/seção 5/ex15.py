d = int(input('Dia da semana 1 a 7: '))
if d == 1:
    print('Domingo')
elif d == 2:
    print('Segunda')
elif d == 3:
    print('Terça')
elif d == 4:
    print('Quarta')
elif d == 5:
    print('Quinta')
elif d == 6:
    print('Sexta')
elif d == 7:
    print('Sábado')
else:
    print('Dia inválido')
